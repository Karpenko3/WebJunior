// --- Налаштування екрана ---
let canvas = document.getElementById("canvas"); // Знаходимо полотно (canvas) у HTML-документі
let context = canvas.getContext("2d");           // Отримуємо контекст малювання (наш "пензлик")

canvas.width = 256;  // Встановлюємо ширину ігрового поля
canvas.height = 512; // Встановлюємо висоту ігрового поля

// --- Завантаження картинок ---
let bird = new Image();       // Створюємо об'єкт для картинки пташки
let back = new Image();       // Створюємо об'єкт для заднього фону
let road = new Image();       // Створюємо об'єкт для дороги (землі)
let pipeUp = new Image();     // Створюємо об'єкт для верхньої труби
let pipeBottom = new Image(); // Створюємо об'єкт для нижньої труби

bird.src = "img/bird.png";             // Вказуємо шлях до файлу пташки
back.src = "img/back.png";             // Вказуємо шлях до файлу фону
road.src = "img/road.png";             // Вказуємо шлях до файлу дороги
pipeUp.src = "img/pipeUp.png";         // Вказуємо шлях до файлу верхньої труби
pipeBottom.src = "img/pipeBottom.png"; // Вказуємо шлях до файлу нижньої труби

// --- Завантаження звуків ---
let fly_audio = new Audio();   // Створюємо об'єкт для звуку помаху крил
let score_audio = new Audio(); // Створюємо об'єкт для звуку отримання очка

fly_audio.src = "audio/fly.mp3";     // Шлях до аудіофайлу польоту
score_audio.src = "audio/score.mp3"; // Шлях до аудіофайлу переможного звуку

// --- Фізика та змінні стану ---
let velY = 0;      // Поточна вертикальна швидкість пташки
let gravity = 0.2; // Сила тяжіння, яка тягне пташку вниз кожного кадру

let xPos = 10;     // Позиція пташки по горизонталі (завжди однакова)
let yPos = 150;    // Початкова позиція пташки по вертикалі
let gap = 110;     // Розмір вільного простору між трубами, куди має пролетіти пташка
let score = 0;     // Лічильник набраних балів

let pipe = [];         // Створюємо порожній масив для зберігання координат труб
pipe[0] = {            // Додаємо першу трубу, щоб гра почалася не з порожнього екрана
    x: canvas.width,   // Перша труба з'явиться за правим краєм екрана
    y: 0               // Початкова висота труби (від самого верху)
};

// --- Головна функція малювання (виконується циклічно) ---
function draw() {
    context.drawImage(back, 0, 0); // Малюємо задній фон на координатах (0,0)

    for (let i = 0; i < pipe.length; i++) { // Цикл проходить через усі труби в нашому списку
        // Малюємо верхню трубу
        context.drawImage(pipeUp, pipe[i].x, pipe[i].y); 
        // Малюємо нижню трубу (висота верхньої + просвіт gap)
        context.drawImage(pipeBottom, pipe[i].x, pipe[i].y + pipeUp.height + gap);

        pipe[i].x--; // Зменшуємо координату X труби, щоб вона рухалася вліво

        // Коли труба проїхала частину шляху (100 пікселів), створюємо нову трубу
        if (pipe[i].x == 100) { 
            pipe.push({ // Додаємо новий об'єкт у масив труб
                x: canvas.width, // Нова труба з'явиться справа
                y: Math.floor(Math.random() * pipeUp.height) - pipeUp.height // Випадкова висота для різноманітності
            });
        }

        // ОПТИМІЗАЦІЯ: Якщо труба повністю вилетіла за екран (ліворуч)
        if (pipe[i].x + pipeUp.width < 0) {
            pipe.shift(); // Видаляємо найпершу (найлівішу) трубу з масиву, щоб не засмічувати пам'ять
        }

        // ПЕРЕВІРКА ЗІТКНЕНЬ:
        if (xPos + bird.width >= pipe[i].x &&                         // Пташка торкнулася лівого краю труби
            xPos <= pipe[i].x + pipeUp.width &&                       // Пташка все ще знаходиться в межах ширини труби
            (yPos <= pipe[i].y + pipeUp.height ||                     // Пташка врізалася у верхню трубу
             yPos + bird.height >= pipe[i].y + pipeUp.height + gap) || // Пташка врізалася у нижню трубу
            yPos + bird.height >= canvas.height - road.height) {      // Пташка впала на дорогу
            
            location.reload(); // Якщо будь-яка умова справдилася — перезавантажуємо сторінку (програш)
        }

        // ПІДРАХУНОК БАЛІВ:
        if (pipe[i].x == 5) {    // Якщо середина пташки пройшла лінію труби (координата x = 5)
            score++;             // Збільшуємо рахунок на 1
            score_audio.play();  // Відтворюємо звук успіху
        }
    }

    context.drawImage(road, 0, canvas.height - road.height); // Малюємо дорогу поверх труб знизу
    context.drawImage(bird, xPos, yPos);                     // Малюємо пташку на її поточних координатах

    // Оновлення фізики польоту:
    velY += gravity; // Швидкість падіння збільшується через гравітацію
    yPos += velY;    // Координата висоти змінюється відповідно до поточної швидкості

    // Малювання тексту рахунку:
    context.fillStyle = "#000";             // Встановлюємо колір тексту (чорний)
    context.font = "24px Verdana";          // Встановлюємо шрифт та розмір
    context.fillText("Score: " + score, 10, canvas.height - 20); // Пишемо текст балів внизу екрана
}

// --- Керування ---
canvas.addEventListener("mousedown", moveUp); // Слухаємо натискання миші по полотну

function moveUp() {
    velY = -4;         // Надаємо пташці від'ємну швидкість (штовхаємо її вгору)
    fly_audio.play();  // Відтворюємо звук помаху крил
}

// Запускаємо ігровий цикл: функція draw буде викликатися кожні 20 мілісекунд
setInterval(draw, 20);